
print("Calcular area de un Triangulo")
print()
#Pidiento al usuario base y altura
base = int(input("Introdusca el Area del Triangulo: "))
altura = int(input("Introdusca la Altura del Triangulo: "))
#haciendo la formula para calcular el area del triangulo
area = (base*altura)/2
#Imprimiendo el resultado
print(f"El area del Triangulo es: {area}")